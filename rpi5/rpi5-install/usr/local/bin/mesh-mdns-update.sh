#!/usr/bin/env bash
# ==============================================================================
# Mesh mDNS Updater
# ==============================================================================
# Generates /etc/avahi/hosts from local node IP plus mesh service state
# discovered via /var/run/mesh_node_registry.
# - manet.local / perf.local always point to this node's br0 IP
# - manet.local / perf.local stay in Avahi static hosts
# - service aliases like mumble.local / mtx.local are handled by
#   mesh-mdns-publisher.py with explicit mDNS announcements
# Avahi is restricted to the AP-facing interface, and reflector stays disabled.
# ==============================================================================

set -euo pipefail

REGISTRY_FILE="/var/run/mesh_node_registry"
AVAHI_HOSTS_FILE="/etc/avahi/hosts"
MESH_CONF_FILE="/etc/mesh.conf"

log() {
    echo "[$(date +'%Y-%m-%d %H:%M:%S')] - MESH-MDNS: $1"
}

local_br0_ip() {
    ip -4 -o addr show dev br0 scope global 2>/dev/null | awk '{print $4}' | cut -d/ -f1 | head -n1
}

main() {
    local local_ip tmpfile
    local_ip="$(local_br0_ip)"
    if [ -z "$local_ip" ]; then
        log "br0 has no IPv4 address yet, skipping"
        exit 0
    fi

    tmpfile=$(mktemp)
    {
        echo "# Auto-generated by mesh-mdns-update.sh"
        echo "$local_ip perf.local"
        echo "$local_ip manet.local"
    } > "$tmpfile"

    if cmp -s "$tmpfile" "$AVAHI_HOSTS_FILE" 2>/dev/null; then
        rm -f "$tmpfile"
        exit 0
    fi

    install -m 644 "$tmpfile" "$AVAHI_HOSTS_FILE"
    rm -f "$tmpfile"

    if systemctl is-active --quiet avahi-daemon; then
        systemctl reload-or-restart avahi-daemon || true
    fi

    log "Updated avahi hosts: local=$local_ip"
}

main "$@"
